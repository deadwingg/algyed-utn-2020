#include <iostream>
using namespace std;

int main() {
    int a, b;
    cout << "Enter first operand:";
    cin >> a;
    cout << "Enter second operand:";
    cin >> b;
    cout << "Suma: " << a + b << endl;
    cout << "Resta: " << a - b << endl;
    cout << "Producto: " << a * b << endl;
    cout << "Cociente: " << a / b << endl;
    return 0;
}
